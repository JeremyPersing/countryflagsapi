function populateModalOne(code) {
  if (!code) return;

  let obj = data[code.toLowerCase()];
  if (!obj) return;

  let namesArr = obj?.name;
  let twoLetterCode = obj?.twoLetterCode;
  let threeLetterCode = obj?.threeLetterCode;
  let number = obj?.number;

  document.getElementById("exampleModalLabel").innerText = namesArr[0];

  let namePngs = "",
    nameSvgs = "";
  if (namesArr) {
    for (let i in namesArr) {
      namePngs += `<p>https://countryflagsapi.com/png/${namesArr[
        i
      ].toLowerCase()}</p>`;
      nameSvgs += `<p>https://countryflagsapi.com/svg/${namesArr[
        i
      ].toLowerCase()}</p>`;
    }
  }

  let twoCodePng = "",
    twoCodeSvg = "";
  if (twoLetterCode) {
    twoCodePng = `<p>https://countryflagsapi.com/png/${twoLetterCode.toLowerCase()}</p>`;
    twoCodeSvg = `<p>https://countryflagsapi.com/svg/${twoLetterCode.toLowerCase()}</p>`;
  }

  let threeCodePng = "",
    threeCodeSvg = "";
  if (threeLetterCode) {
    threeCodePng = `<p>https://countryflagsapi.com/png/${threeLetterCode.toLowerCase()}</p>`;
    threeCodeSvg = `<p>https://countryflagsapi.com/svg/${threeLetterCode.toLowerCase()}</p>`;
  }

  let numPng = "",
    numSvg = "";
  if (number) {
    numPng = `<p>https://countryflagsapi.com/png/${number.toLowerCase()}</p>`;
    numSvg = `<p>https://countryflagsapi.com/svg/${number.toLowerCase()}</p>`;
  }

  let modalBody = `<div>
      <p>
        <b>PNG</b>
      </p>
      ${namePngs}
      ${twoCodePng}
      ${threeCodePng}
      ${numPng}
      <br></br>
      <p>
        <b>SVG</b>
      </p>
      ${nameSvgs}
      ${twoCodeSvg}
      ${threeCodeSvg}
      ${numSvg}
    </div>`;

  document.getElementById("modalBody").innerHTML = modalBody;
}

function populateModalTwo(code, type) {
  if (!code) return;

  let obj = data[code.toLowerCase()];
  if (!obj) return;

  let fileType = "PNG";
  if (type === "svg") fileType = "SVG";
  document.getElementById("modalTwoLabel").innerText =
    obj?.name[0] + " " + fileType;

  let img = `<img src="https://countryflagsapi.com/${type}/${code.toLowerCase()}" alt="${
    obj?.name[0]
  } flag"/>`;

  let body = `<div>
      <p>Note that the other endpoints will work for the source as well</p>
      <code><xmp>${img}</xmp></code>
    </div>`;

  document.getElementById("modalTwoBody").innerHTML = body;
  document.getElementById("modalTwoBody").value = img;
}

function copyImgTag() {
  let tag = document.getElementById("modalTwoBody").value;
  if (!navigator.clipboard) {
    alert("Your browser doesn't support this feature");
    return;
  }
  navigator.clipboard
    .writeText(tag)
    .catch(() => alert("Unable to copy to clipboard"));
}

